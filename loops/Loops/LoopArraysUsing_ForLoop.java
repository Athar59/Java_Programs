
public class LoopArraysUsing_ForLoop {
    public static void main(String[] args) {
        int[] numbers = {1, 2, 3, 4, 5};
        for (int i = 0; i < numbers.length; i++) {
            System.out.println("length of array: "+numbers[i]);
        }
    }
}


//OUTPUT***************

// length of array: 5
