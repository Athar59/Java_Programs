
public class NullVariablesExercise {
    public static void main(String[] args) {
        String name = null;

        if(name == null) {
            System.out.println("The name variable is null.");
        } else {
            System.out.println("The name variable has a value: " + name);
        }
    }
}


//OUTPUT**********************************

//The name variable is null.
